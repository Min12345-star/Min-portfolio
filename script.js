const folders = document.querySelectorAll(".folder");
const projectTriggers = document.querySelectorAll("[data-project-trigger]");
const projectPages = document.querySelectorAll("[data-project-page]");
const languageToggle = document.querySelector(".lang-toggle");
const savedLanguage = localStorage.getItem("portfolioLanguage") || "en";

function setLanguage(language) {
  const dictionary = translations[language] || translations.en;
  document.documentElement.lang = language === "zh" ? "zh-CN" : "en";
  document.documentElement.dataset.lang = language;

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  localStorage.setItem("portfolioLanguage", language);
}

folders.forEach((folder) => {
  folder.addEventListener("pointermove", (event) => {
    const rect = folder.getBoundingClientRect();
    const x = event.clientX - rect.left - rect.width / 2;
    const y = event.clientY - rect.top - rect.height / 2;
    folder.style.setProperty("--tilt-x", `${y / -12}deg`);
    folder.style.setProperty("--tilt-y", `${x / 14}deg`);
  });

  folder.addEventListener("pointerleave", () => {
    folder.style.setProperty("--tilt-x", "0deg");
    folder.style.setProperty("--tilt-y", "0deg");
  });
});

projectTriggers.forEach((trigger) => {
  const activateProject = () => {
    const project = trigger.dataset.projectTrigger;

    projectTriggers.forEach((item) => {
      const isActive = item === trigger;
      item.classList.toggle("is-active", isActive);
      item.setAttribute("aria-selected", String(isActive));
    });

    projectPages.forEach((page) => {
      page.classList.toggle("is-active", page.dataset.projectPage === project);
    });
  };

  trigger.addEventListener("mouseenter", activateProject);
  trigger.addEventListener("focus", activateProject);
  trigger.addEventListener("click", () => {
    activateProject();
    if (trigger.dataset.projectPdf) {
      window.location.href = trigger.dataset.projectPdf;
    }
  });
});

if (languageToggle) {
  languageToggle.addEventListener("click", () => {
    const nextLanguage = document.documentElement.dataset.lang === "zh" ? "en" : "zh";
    setLanguage(nextLanguage);
  });
}

setLanguage(savedLanguage);
